// Calcola la somma e la media dei primi 10 numeri

// Inizio a contare da 0
let sum = 0;

// Sommo tutti i numeri
for( let i = 1; i <= 10; i++ ) {
    sum += i;
}

// Stampo in console la somma degli elementi
console.log(sum);

// Calcolo la media degli elementi
sum = sum / 10;

// Stampo in console la media degli elementi
console.log(sum);